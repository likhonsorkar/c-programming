#include <stdio.h>
int main()
{
    // Variable declare
    int a,b,c;
    // input 1st number
    printf("Enter 1st number : a = \n");
    scanf("%d",&a);

    // input 2nd number
    printf("Enter 2nd number: b =\n");
    scanf("%d",&b);

    // input 3rd number
    printf("Enter 3rd number: c =\n");
    scanf("%d",&c);

    // print three number
    printf("1st number : a = %d \n2nd number : b = %d \n3rd number : c = %d", a,b,c);
    return 0;
}